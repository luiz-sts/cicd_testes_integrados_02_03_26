// add(a, b): Retorna a soma de a e b.
// subtract(a, b): Retorna a diferença entre a e b.
// multiply(a, b): Retorna o produto de a e b.
// divide(a, b): Retorna a divisão de a por b.

function add(a, b) {

return a+b;
}


function subtract(a, b) {

return a-b;
}

function multiply(a, b) {

return a*b;
}

function divide(a, b) {

return a/b;
}

module.exports = {
    add,
    divide,
    multiply,
    subtract
}
